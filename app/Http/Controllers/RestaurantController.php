    {
        $data = $request->all();
        $newRestaurant = new Restaurant();
        $newRestaurant->fill($data);
        $newRestaurant->password = bcrypt($data['password']);
        $img_path = Storage::put('uploads', $data['image']);
        $newRestaurant->image = $img_path;
    }

